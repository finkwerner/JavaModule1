package Encapsulation;

public class Trainee {

    private String fullName;

    private String ID;

    public void setFullName(String newFullname){
        fullName = newFullname;
    }

    public String getFullName(){
        return fullName;
    }

    public void setID(String newID){
        ID = newID;
    }

    public String getID(){
        return ID;
    }


}
